const initialData = {
    cart_items: [], 
    shipping_info: {}
}

const cartReducer = (state = initialData, action) => {
    switch(action.type){
        case "SAVE_SHIPPING_INFO":
            return {
                ...state,
                shipping_info: action.payload
            }

        case "ADD_TO_CART":
            return {...state,
                cart_items: [...state.cart_items, action.payload]
            }

        case "REMOVE_FROM_CART":
            return {
                ...state,
                cart_items: state.cart_items.filter(item => item.product != action.payload)
            }

        case "UPDATE_CART":
            console.log(action)
            return {
                ...state, 
                cart_items: state.cart_items.map(item => 
                    item.product === action.payload.product ? action.payload : item 
                )
            }

        case "CLEAR_CART":
            return {
                ...state, 
                cart_items: []
            }

        default:
            return state
    }
}

export default cartReducer